def generate_responses(
    model,
    prompts: List[str],
    max_new_tokens: int = 100,
    temperature: float = 0.7,
    show_progress: bool = True,
) -> List[Dict]:
    """
    Generate responses from a model for a set of prompts.

    Returns:
        List of dicts with 'prompt', 'response', 'model' keys
    """
    results = []
    iterator = tqdm(prompts, desc="Generating") if show_progress else prompts

    for prompt in iterator:
        try:
            tokens = model.to_tokens(prompt, prepend_bos=True)
            try:
                # Primary: temperature sampling
                output_tokens = model.generate(
                    tokens,
                    max_new_tokens=max_new_tokens,
                    temperature=temperature,
                    do_sample=temperature > 0,
                )
            except Exception:
                # Fallback: greedy decoding (avoids Categorical NaN constraint
                # check that fires on some models, e.g. Gemma-3-4b)
                output_tokens = model.generate(
                    tokens,
                    max_new_tokens=max_new_tokens,
                    temperature=0,
                    do_sample=False,
                )
            # Decode only the new tokens
            new_tokens = output_tokens[0, tokens.shape[1]:]
            response = model.to_string(new_tokens)
            results.append({
                "prompt": prompt,
                "response": response.strip(),
                "model": model.cfg.model_name,
            })
        except Exception as e:
            print(f"Error generating for '{prompt[:40]}...': {e}")
            continue

    return results


def save_responses(responses: List[Dict], model_name: str, output_dir: str = "data/responses"):
    """Save generated responses to disk."""
    Path(output_dir).mkdir(parents=True, exist_ok=True)
    safe_name = model_name.replace("/", "_")
    filepath = Path(output_dir) / f"responses_{safe_name}.json"
    with open(filepath, "w") as f:
        json.dump(responses, f, indent=2)
    print(f"Saved {len(responses)} responses to {filepath}")


def load_responses(model_name: str, output_dir: str = "data/responses") -> List[Dict]:
    """Load previously generated responses."""
    safe_name = model_name.replace("/", "_")
    filepath = Path(output_dir) / f"responses_{safe_name}.json"
    with open(filepath, "r") as f:
        return json.load(f)


