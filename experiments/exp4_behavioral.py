def create_attribution_task(
    target_response: Dict,
    distractor_responses: List[Dict],
    rng: random.Random,
) -> Dict:
    """
    Create a single self-attribution task.

    Args:
        target_response: Response from the target model
        distractor_responses: Responses from other models
        rng: Random number generator

    Returns:
        Dict with attribution_prompt, correct_answer, options
    """
    options = [target_response] + distractor_responses
    rng.shuffle(options)

    labels = ["A", "B", "C", "D"][:len(options)]
    correct_idx = options.index(target_response)
    correct_label = labels[correct_idx]

    option_text = ""
    for label, opt in zip(labels, options):
        option_text += f"\n{label}: {opt['response']}\n"

    attribution_prompt = (
        f"Earlier, I asked several AI assistants: \"{target_response['prompt']}\"\n\n"
        f"Here are their responses:{option_text}\n"
        f"One of these responses was generated by you. Which one? "
        f"Answer with just the letter ({', '.join(labels)})."
    )

    return {
        "prompt": attribution_prompt,
        "correct": correct_label,
        "target_model": target_response["model"],
        "distractor_models": [d["model"] for d in distractor_responses],
        "base_prompt": target_response["prompt"],
    }


def create_task_battery(
    target_responses: List[Dict],
    all_model_responses: Dict[str, List[Dict]],
    difficulty: str = "easy",
    target_model: str = "",
    n_tasks: int = 50,
    seed: int = 42,
) -> List[Dict]:
    """
    Create a battery of attribution tasks at specified difficulty.

    Args:
        target_responses: Responses from the model being tested
        all_model_responses: Dict mapping model_name to response list
        difficulty: "easy", "medium", "hard", "hardest"
        target_model: Name of the target model
        n_tasks: Number of tasks to create
        seed: Random seed

    Returns:
        List of attribution task dicts
    """
    rng = random.Random(seed)
    tasks = []
    target_family = detect_model_family(target_model)

    # Build prompt-indexed lookup
    target_by_prompt = {r["prompt"]: r for r in target_responses}

    # Select distractors based on difficulty
    other_models = {k: v for k, v in all_model_responses.items() if k != target_model}

    if difficulty == "easy":
        # Different model families
        distractor_pool = {
            k: v for k, v in other_models.items()
            if detect_model_family(k) != target_family
        }
    elif difficulty == "medium":
        # Same family, different sizes
        distractor_pool = {
            k: v for k, v in other_models.items()
            if detect_model_family(k) == target_family
        }
    elif difficulty == "hard":
        # Paraphrased own responses (use same model, different phrasing)
        distractor_pool = other_models  # Will need paraphrasing logic
    elif difficulty == "hardest":
        # Same model, different temperature (pre-generated)
        distractor_pool = {
            k: v for k, v in other_models.items()
            if k.startswith(target_model + "_temp")
        }
    else:
        distractor_pool = other_models

    if not distractor_pool:
        distractor_pool = other_models

    distractor_models = list(distractor_pool.keys())

    for target_resp in target_responses[:n_tasks]:
        prompt = target_resp["prompt"]

        # Find matching distractors
        distractors = []
        for dm in distractor_models:
            dm_by_prompt = {r["prompt"]: r for r in distractor_pool.get(dm, [])}
            if prompt in dm_by_prompt:
                distractors.append(dm_by_prompt[prompt])
            if len(distractors) >= 2:
                break

        if len(distractors) < 2:
            continue

        task = create_attribution_task(target_resp, distractors[:2], rng)
        task["difficulty"] = difficulty
        tasks.append(task)

    return tasks


