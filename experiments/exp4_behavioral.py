# Evaluation
# ---------------------------------------------------------------------------

def evaluate_attribution(
    model,
    tasks: List[Dict],
    show_progress: bool = True,
) -> Dict:
    """
    Evaluate model's self-attribution accuracy.

    Args:
        model: HookedTransformer model (the model being tested)
        tasks: List of attribution task dicts

    Returns:
        Dict with accuracy, p_value, significance
    """
    correct = 0
    total = 0
    details = []

    # Pre-compute token IDs for A, B, C and cache sparse unembedding weights.
    # This avoids allocating the full [seq_len, vocab_size] logits tensor, which
    # can be ~100 MB for Llama's 128K vocab and causes OOM on tight 70B GPU setups.
    letter_tok_ids = {}
    for letter in ["A", "B", "C"]:
        toks = model.to_tokens(letter, prepend_bos=False)
        if toks.shape[1] > 0:
            letter_tok_ids[letter] = toks[0, 0].item()

    letters = list(letter_tok_ids.keys())
    tok_id_list = [letter_tok_ids[l] for l in letters]

    # Sparse unembedding: W_U[:, tok_ids] — shape [d_model, n_letters]
    # Keep on CPU; move to device per-task as needed.
    W_U_sparse = model.unembed.W_U[:, tok_id_list].detach().cpu()
    b_U_sparse = None
    if model.unembed.b_U is not None:
        b_U_sparse = model.unembed.b_U[tok_id_list].detach().cpu()

    n_layers = model.cfg.n_layers

    iterator = tqdm(tasks, desc="Evaluating") if show_progress else tasks

    for task in iterator:
        residual = None
        try:
            tokens = model.to_tokens(task["prompt"], prepend_bos=True)

            # Run forward pass stopping before unembed to avoid allocating the
            # full [batch, seq_len, vocab_size] logits tensor (~100 MB for 128K vocab).
            with torch.no_grad():
                residual = model(tokens, stop_at_layer=n_layers)

            # Extract last-token residual and apply final layer norm.
            last_res = residual[0, -1, :]        # [d_model]  on last GPU
            del residual
            residual = None
            torch.cuda.empty_cache()

            last_res = model.ln_final(last_res.unsqueeze(0).unsqueeze(0))  # [1,1,d_model]
            last_res = last_res.squeeze()                                   # [d_model]

            # Sparse logit computation for A/B/C only.
            W = W_U_sparse.to(last_res.device)  # [d_model, n_letters]
            sparse_logits = last_res @ W         # [n_letters]
            if b_U_sparse is not None:
                sparse_logits = sparse_logits + b_U_sparse.to(last_res.device)

            probs = {letters[i]: sparse_logits[i].item() for i in range(len(letters))}

            # Predict = highest logit letter
            predicted = max(probs, key=probs.get) if probs else "?"

            is_correct = predicted == task["correct"]
            if is_correct:
                correct += 1
            total += 1

            details.append({
                "base_prompt": task["base_prompt"],
                "predicted": predicted,
                "correct": task["correct"],
                "is_correct": is_correct,
                "logits": {k: float(v) for k, v in probs.items()},
            })

        except Exception as e:
            print(f"Error: {e}")
            torch.cuda.empty_cache()
            continue
        finally:
            if residual is not None:
                del residual
            torch.cuda.empty_cache()

    if total == 0:
        return {"accuracy": 0, "n": 0, "above_chance": False, "p_value": 1.0, "details": []}

    accuracy = correct / total
    chance = 1 / 3

    # Binomial test against chance
    test_result = binomtest(correct, total, chance, alternative="greater")
    p_value = test_result.pvalue

    return {
        "accuracy": float(accuracy),
        "correct": correct,
        "total": total,
        "n": total,
        "above_chance": p_value < 0.05,
        "p_value": float(p_value),
        "chance": chance,
        "details": details,
    }


def run_behavioral_experiment(
    model,
    target_responses: List[Dict],
    all_model_responses: Dict[str, List[Dict]],
    target_model: str,
    n_tasks_per_difficulty: int = 50,
    seed: int = 42,
) -> Dict:
    """
    Run the full behavioral self-recognition experiment.

    Args:
        model: HookedTransformer model
        target_responses: This model's pre-generated responses
        all_model_responses: All models' pre-generated responses
        target_model: This model's name
        n_tasks_per_difficulty: Tasks per difficulty level
        seed: Random seed

    Returns:
        Full results dict
    """
    results = {
        "model": target_model,
        "difficulties": {},
    }

    for difficulty in ["easy", "medium", "hard"]:
        print(f"\n--- Difficulty: {difficulty} ---")
        tasks = create_task_battery(
            target_responses, all_model_responses,
            difficulty=difficulty,
            target_model=target_model,
            n_tasks=n_tasks_per_difficulty,
            seed=seed,
        )

        if not tasks:
            print(f"  No tasks created for {difficulty} (missing distractor responses?)")
            results["difficulties"][difficulty] = {
                "accuracy": 0, "n": 0, "above_chance": False, "p_value": 1.0,
            }
            continue

        eval_result = evaluate_attribution(model, tasks)
        results["difficulties"][difficulty] = eval_result

        sig = "*" if eval_result["above_chance"] else ""
        print(f"  Accuracy: {eval_result['accuracy']:.3f} "
              f"({eval_result['correct']}/{eval_result['total']}) "
              f"p={eval_result['p_value']:.4f}{sig}")

    # Interpret
    results["interpretation"] = _interpret_behavioral(results["difficulties"])

    print("\n" + "=" * 60)
    print("BEHAVIORAL SELF-RECOGNITION RESULTS")
    print("=" * 60)
    for diff, data in results["difficulties"].items():
        sig = "*" if data.get("above_chance") else ""
        print(f"  {diff:<10} acc={data['accuracy']:.3f} p={data['p_value']:.4f}{sig} (n={data['n']})")
    print(f"\nInterpretation: {results['interpretation']}")
    print("=" * 60)

    return results


def _interpret_behavioral(difficulties: Dict) -> str:
    """Interpret behavioral results per outcome table."""
    easy = difficulties.get("easy", {})
    hard = difficulties.get("hard", {})

    easy_sig = easy.get("above_chance", False)
    hard_sig = hard.get("above_chance", False)

    if easy_sig and hard_sig:
        return "True self-recognition: distinguishes self from similar models"
    elif easy_sig and not hard_sig:
        return "Style only: recognizes family style, not self"
    elif not easy_sig:
        return "Null: no self-recognition capability detected"
    else:
        return "Ambiguous pattern"


def main():
    parser = argparse.ArgumentParser(description="Experiment 4: Behavioral Self-Recognition")
    parser.add_argument("--model", type=str, default="Qwen/Qwen3-0.6B")
    parser.add_argument("--device", type=str, default="cuda:2")
    parser.add_argument("--n-devices", type=int, default=1)
    parser.add_argument("--mode", type=str, choices=["generate", "evaluate", "both"], default="both")
    parser.add_argument("--temperature", type=float, default=0.7)
    parser.add_argument("--max-new-tokens", type=int, default=100)
    parser.add_argument("--n-tasks", type=int, default=50)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--prompts-file", type=str, default="data/prompts/attribution_prompts.json")
    parser.add_argument("--responses-dir", type=str, default="data/responses")
    parser.add_argument("--output-dir", type=str, default="data/results")
    args = parser.parse_args()

    set_seed(args.seed)

    model = load_model(args.model, device=args.device, n_devices=args.n_devices)

    if args.mode in ("generate", "both"):
        # Load base prompts
        prompt_data = load_prompts(args.prompts_file)
        base_prompts = prompt_data["prompts"]

        # Generate responses
        print(f"\nGenerating responses from {args.model}...")
        responses = generate_responses(
            model, base_prompts,
            max_new_tokens=args.max_new_tokens,
            temperature=args.temperature,
        )
        save_responses(responses, args.model, args.responses_dir)

    if args.mode in ("evaluate", "both"):
        # Load all available responses
        responses_dir = Path(args.responses_dir)
        all_responses = {}

        for resp_file in responses_dir.glob("responses_*.json"):
            with open(resp_file) as f:
                resps = json.load(f)
            if resps:
                model_name = resps[0]["model"]
                all_responses[model_name] = resps

        if args.model not in all_responses and model.cfg.model_name in all_responses:
            target_model = model.cfg.model_name
        else:
            target_model = args.model

        if target_model not in all_responses:
            print(f"No responses found for {target_model}. Run with --mode generate first.")
            return

        target_responses = all_responses[target_model]

        results = run_behavioral_experiment(
            model, target_responses, all_responses,
            target_model=target_model,
            n_tasks_per_difficulty=args.n_tasks,
            seed=args.seed,
        )

        safe_name = args.model.replace("/", "_")
        save_results(results, f"exp4_behavioral_{safe_name}", args.output_dir)


if __name__ == "__main__":
    main()
